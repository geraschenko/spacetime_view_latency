#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR=$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)
cd "$ROOT_DIR"

LISTEN_ADDR=${SPACETIME_LISTEN_ADDR:-127.0.0.1:3000}
SERVER_URL=${SPACETIME_SERVER_URL:-http://$LISTEN_ADDR}
WASM=target/wasm32-unknown-unknown/release/module.wasm
CLIENT=target/release/roundtrip_latency_test
RESULTS_DIR="$ROOT_DIR/results"

for command in spacetime cargo curl; do
    command -v "$command" >/dev/null || {
        echo "Missing required command: $command" >&2
        exit 1
    }
done

if [[ ${SKIP_BUILD:-0} != 1 ]]; then
    ./run_setup.sh
fi

[[ -f "$WASM" ]] || { echo "Missing $WASM; run ./run_setup.sh" >&2; exit 1; }
[[ -x "$CLIENT" ]] || { echo "Missing $CLIENT; run ./run_setup.sh" >&2; exit 1; }

SPACETIME_VERSION=$(spacetime --version 2>&1 | sed -n 's/.*spacetimedb tool version \([^;]*\);.*/\1/p' | tail -1)
[[ -n $SPACETIME_VERSION ]] || { echo "Could not determine SpacetimeDB version" >&2; exit 1; }
COLLECTED_AT=$(date -u +%Y-%m-%dT%H:%M:%SZ)
STAGING_DIR=$(mktemp -d "$ROOT_DIR/.results.XXXXXX")

{
    echo "collected_at=$COLLECTED_AT"
    echo "git_revision=$(git rev-parse HEAD)"
    echo "server_url=$SERVER_URL"
    spacetime --version
    rustc --version
    cargo --version
} > "$STAGING_DIR/metadata.txt"

SERVER_PID=
DATA_DIR=
stop_server() {
    if [[ -n ${SERVER_PID:-} ]]; then
        kill "$SERVER_PID" 2>/dev/null || true
        wait "$SERVER_PID" 2>/dev/null || true
        SERVER_PID=
    fi
    if [[ -n ${DATA_DIR:-} ]]; then
        rm -rf "$DATA_DIR"
        DATA_DIR=
    fi
}
cleanup() {
    stop_server
    if [[ -n ${STAGING_DIR:-} ]]; then
        rm -rf "$STAGING_DIR"
    fi
}
trap cleanup EXIT

run_case() {
    local label=$1
    local subscription=$2
    shift 2

    DATA_DIR=$(mktemp -d "${TMPDIR:-/tmp}/view-latency.XXXXXX")
    local server_log="$DATA_DIR/server.log"

    spacetime start \
        --data-dir "$DATA_DIR/database" \
        --listen-addr "$LISTEN_ADDR" \
        --non-interactive > "$server_log" 2>&1 &
    SERVER_PID=$!

    local ready=false
    for _ in $(seq 1 120); do
        if curl -fsS "$SERVER_URL/v1/ping" >/dev/null 2>&1; then
            ready=true
            break
        fi
        if ! kill -0 "$SERVER_PID" 2>/dev/null; then
            echo "SpacetimeDB exited while starting:" >&2
            tail -100 "$server_log" >&2
            return 1
        fi
        sleep 0.25
    done
    if [[ $ready != true ]]; then
        echo "Timed out waiting for SpacetimeDB:" >&2
        tail -100 "$server_log" >&2
        return 1
    fi

    # Each case gets a fresh data directory, so no clear/reset authorization is needed.
    spacetime publish view-latency \
        --anonymous \
        --server "$SERVER_URL" \
        --bin-path "$WASM" \
        --yes

    "$CLIENT" \
        --server "$SERVER_URL" \
        --subscribe-to "$subscription" \
        "$@" | tee "$STAGING_DIR/$label.txt"

    stop_server
}

run_case view-confirmed view
run_case view-unconfirmed view --no-confirmed-reads
run_case table-confirmed table
run_case table-unconfirmed table --no-confirmed-reads

{
    echo "_Generated by \`update_results.sh\` using SpacetimeDB $SPACETIME_VERSION on $COLLECTED_AT._"
    for label in view-confirmed view-unconfirmed table-confirmed table-unconfirmed; do
        title=${label//-/ }
        echo
        echo "### ${title^}"
        echo
        echo '```text'
        cat "$STAGING_DIR/$label.txt"
        echo '```'
    done
} > "$STAGING_DIR/latest-results.md"

# Only replace committed results after every benchmark succeeds.
rm -rf "$RESULTS_DIR"
mv "$STAGING_DIR" "$RESULTS_DIR"
STAGING_DIR=

README_TMP=$(mktemp "$ROOT_DIR/.README.XXXXXX")
awk -v generated="$RESULTS_DIR/latest-results.md" '
    /<!-- BEGIN GENERATED RESULTS -->/ {
        print
        while ((getline line < generated) > 0) print line
        close(generated)
        replacing = 1
        found_begin = 1
        next
    }
    /<!-- END GENERATED RESULTS -->/ {
        replacing = 0
        found_end = 1
        print
        next
    }
    !replacing { print }
    END { if (!found_begin || !found_end) exit 1 }
' README.md > "$README_TMP" || {
    rm -f "$README_TMP"
    echo "README.md is missing generated-results markers" >&2
    exit 1
}
mv "$README_TMP" README.md

echo "Results written to results/ and README.md updated"
